class AuditPDF(FPDF):
    """Custom PDF class with headers and footers."""

    def __init__(self):
        super().__init__('P', 'mm', 'Letter')
        self.set_auto_page_break(auto=True, margin=25)

    def header(self):
        if self.page_no() == 1:
            return  # cover page has custom header
        self.set_font('Helvetica', 'B', 8)
        self.set_text_color(*GRAY)
        self.cell(0, 5, 'DESTROYER QUANTUM V28.09 OBLIVION  |  BACKTEST AUDIT REPORT', align='L')
        self.cell(0, 5, f'Page {self.page_no()}', align='R', new_x='LMARGIN', new_y='NEXT')
        self.set_draw_color(*LIGHT_GRAY)
        self.line(10, 12, self.w - 10, 12)
        self.ln(5)

    def footer(self):
        self.set_y(-20)
        self.set_draw_color(*LIGHT_GRAY)
        self.line(10, self.get_y(), self.w - 10, self.get_y())
        self.ln(3)
        self.set_font('Helvetica', '', 7)
        self.set_text_color(*GRAY)
        self.cell(0, 5, 'Generated by DESTROYER QUANTUM Audit System  |  Confidential', align='L')
        self.cell(0, 5, f'Page {self.page_no()}/{{nb}}', align='R')

    # ─── Utility helpers ───────────────────────────────────────────────────────
    def section_title(self, num, title):
        """Major section header with colored bar."""
        self.ln(4)
        self.set_fill_color(*DARK_BLUE)
        self.set_text_color(*WHITE)
        self.set_font('Helvetica', 'B', 13)
        self.cell(0, 9, f'  {num}. {title}', fill=True, new_x='LMARGIN', new_y='NEXT')
        self.ln(3)
        self.set_text_color(*BLACK)

    def sub_title(self, title):
        """Sub-section header."""
        self.ln(2)
        self.set_fill_color(*MED_BLUE)
        self.set_text_color(*WHITE)
        self.set_font('Helvetica', 'B', 10)
        self.cell(0, 7, f'  {title}', fill=True, new_x='LMARGIN', new_y='NEXT')
        self.ln(2)
        self.set_text_color(*BLACK)

    def kv_row(self, label, value, bold_value=False, value_color=None):
        """Key-value row with label on left, value on right."""
        self.set_font('Helvetica', '', 9)
        self.set_text_color(*GRAY)
        self.cell(80, 5.5, f'  {label}:', align='L')
        style = 'B' if bold_value else ''
        if value_color:
            self.set_text_color(*value_color)
        else:
            self.set_text_color(*BLACK)
        self.set_font('Helvetica', style, 9)
        self.cell(0, 5.5, str(value), align='L', new_x='LMARGIN', new_y='NEXT')

    def alert_box(self, text, level='info'):
        """Colored alert/info box."""
        colors = {
            'info':    ((230, 240, 255), MED_BLUE),
            'success': ((230, 250, 235), GREEN),
            'warning': ((255, 245, 230), GOLD),
            'danger':  ((255, 235, 235), RED),
        }
        bg, fg = colors.get(level, colors['info'])
        self.set_fill_color(*bg)
        self.set_draw_color(*fg)
        self.set_text_color(*fg)
        self.set_font('Helvetica', 'B', 9)
        x = self.get_x()
        y = self.get_y()
        self.rect(10, y, self.w - 20, 8, style='DF')
        self.set_xy(12, y + 1.5)
        self.cell(0, 5, text, new_x='LMARGIN', new_y='NEXT')
        self.set_text_color(*BLACK)
        self.ln(2)

    def table_header(self, cols, widths):
        """Print a table header row."""
        self.set_fill_color(*DARK_BLUE)
        self.set_text_color(*WHITE)
        self.set_font('Helvetica', 'B', 8)
        for col, w in zip(cols, widths):
            self.cell(w, 6, col, border=0, align='C', fill=True)
        self.ln()
        self.set_text_color(*BLACK)

    def table_row(self, values, widths, aligns=None, colors=None, bold=False):
        """Print a table data row."""
        if aligns is None:
            aligns = ['L'] * len(values)
        if self.get_y() + 6 > self.h - 25:
            self.add_page()
        # Alternate row shading
        row_num = int((self.get_y() - 50) / 6)
        if row_num % 2 == 0:
            self.set_fill_color(*VERY_LIGHT)
        else:
            self.set_fill_color(*WHITE)
        style = 'B' if bold else ''
        self.set_font('Helvetica', style, 8)
        for i, (val, w, a) in enumerate(zip(values, widths, aligns)):
            if colors and i < len(colors) and colors[i]:
                self.set_text_color(*colors[i])
            else:
                self.set_text_color(*BLACK)
            self.cell(w, 6, str(val), border=0, align=a, fill=True)
        self.ln()
        self.set_text_color(*BLACK)


